
public class Ex_11 {
	int first;
	int second;
	
	public void setFirst(int first) {
		this.first = first;
	}
	public void setSecond(int second) {
		this.second = second;
	}
	
	public int add() {
		return this.first+this.second;
	}
	
	public int sub() {
		return this.first-this.second;
	}

}
